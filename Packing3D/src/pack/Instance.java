package pack;

public class Instance
{
	private int _L;
	private int _W;
	private int _H;
	private int _l;
	private int _w;
	private int _h;
	
	public Instance(int L, int W, int H, int l, int w, int h)
	{
		_L = L;
		_W = W;
		_H = H;
		_l = l;
		_w = w;
		_h = h;
	}
	
	public int getL()
	{
		return _L;
	}
	
	public int getW()
	{
		return _W;
	}
	
	public int getH()
	{
		return _H;
	}
	
	public int getl()
	{
		return _l;
	}
	
	public int getw()
	{
		return _w;
	}
	
	public int geth()
	{
		return _h;
	}
}
